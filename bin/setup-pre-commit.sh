#!/usr/bin/env bash
# Akibara pre-commit hook installer.
#
# Sprint 2 B-S2-SETUP-01 — defensa local antes de push.
# Memoria: project_quality_gates_stack.md (3 grep gates + gitleaks).
#
# Hook corre 4 checks rápidos sobre staged files:
#   1. grep-voseo.sh --staged   (rompe commit si voseo detectado)
#   2. grep-secrets.sh --staged (rompe commit si secrets detectados)
#   3. grep-claims.sh --staged  (warning, no rompe commit)
#   4. gitleaks (si binary disponible localmente)
#
# Tiempo target: <5s en commits típicos.
#
# Uso:
#   bash bin/setup-pre-commit.sh             # instalar hook
#   bash bin/setup-pre-commit.sh --uninstall # remover hook
#   bash bin/setup-pre-commit.sh --status    # verify si está instalado

set -euo pipefail

PROJECT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
cd "$PROJECT_DIR"

HOOK_PATH="$PROJECT_DIR/.git/hooks/pre-commit"

if [[ -t 1 ]]; then
  C_OK=$'\033[32m'; C_FAIL=$'\033[31m'; C_WARN=$'\033[33m'; C_DIM=$'\033[2m'; C_OFF=$'\033[0m'
else
  C_OK=""; C_FAIL=""; C_WARN=""; C_DIM=""; C_OFF=""
fi

mode="${1:-install}"

if [[ "$mode" == "--status" ]]; then
  if [[ -f "$HOOK_PATH" ]]; then
    if grep -q "Akibara pre-commit" "$HOOK_PATH" 2>/dev/null; then
      echo "${C_OK}✓ Pre-commit hook installed at $HOOK_PATH${C_OFF}"
      exit 0
    else
      echo "${C_WARN}⚠️  Hook existe pero NO es de Akibara (different installer)${C_OFF}"
      exit 1
    fi
  else
    echo "${C_DIM}Hook NO instalado.${C_OFF}"
    exit 1
  fi
fi

if [[ "$mode" == "--uninstall" ]]; then
  if [[ -f "$HOOK_PATH" ]] && grep -q "Akibara pre-commit" "$HOOK_PATH" 2>/dev/null; then
    rm "$HOOK_PATH"
    echo "${C_OK}✓ Pre-commit hook uninstalled${C_OFF}"
  else
    echo "${C_DIM}Nothing to uninstall.${C_OFF}"
  fi
  exit 0
fi

# Default: install
mkdir -p "$PROJECT_DIR/.git/hooks"

cat > "$HOOK_PATH" <<'HOOK_EOF'
#!/usr/bin/env bash
# Akibara pre-commit hook (auto-generated by bin/setup-pre-commit.sh)
# Manual edit: re-run installer to regenerate.

set -uo pipefail

PROJECT_DIR="$(git rev-parse --show-toplevel)"
cd "$PROJECT_DIR"

if [[ -t 1 ]]; then
  C_OK=$'\033[32m'; C_FAIL=$'\033[31m'; C_WARN=$'\033[33m'; C_OFF=$'\033[0m'
else
  C_OK=""; C_FAIL=""; C_WARN=""; C_OFF=""
fi

failures=0

# 1. Voseo (HARD fail)
if [[ -x "$PROJECT_DIR/bin/grep-voseo.sh" ]]; then
  if ! bash "$PROJECT_DIR/bin/grep-voseo.sh" --staged; then
    echo "${C_FAIL}❌ pre-commit: voseo rioplatense bloquea commit${C_OFF}"
    failures=$((failures + 1))
  fi
fi

# 2. Secrets (HARD fail)
if [[ -x "$PROJECT_DIR/bin/grep-secrets.sh" ]]; then
  if ! bash "$PROJECT_DIR/bin/grep-secrets.sh" --staged; then
    echo "${C_FAIL}❌ pre-commit: secrets bloquean commit${C_OFF}"
    failures=$((failures + 1))
  fi
fi

# 3. Claims (WARNING only — no rompe)
if [[ -x "$PROJECT_DIR/bin/grep-claims.sh" ]]; then
  bash "$PROJECT_DIR/bin/grep-claims.sh" --staged || true
fi

# 4. Gitleaks (si binary disponible)
if command -v gitleaks >/dev/null 2>&1; then
  if ! gitleaks protect --staged --no-banner --redact 2>/dev/null; then
    echo "${C_FAIL}❌ pre-commit: gitleaks detectó secrets${C_OFF}"
    failures=$((failures + 1))
  fi
fi

if [[ $failures -gt 0 ]]; then
  echo ""
  echo "${C_FAIL}Commit BLOCKED ($failures gates failed).${C_OFF}"
  echo "${C_WARN}Skip hook (NO recomendado): git commit --no-verify${C_OFF}"
  exit 1
fi

echo "${C_OK}✓ Pre-commit gates green${C_OFF}"
exit 0
HOOK_EOF

chmod +x "$HOOK_PATH"

echo "${C_OK}✓ Pre-commit hook installed at $HOOK_PATH${C_OFF}"
echo ""
echo "${C_DIM}Gates aplicados (staged files):${C_OFF}"
echo "${C_DIM}  - grep-voseo.sh    HARD fail (voseo rioplatense bloquea)${C_OFF}"
echo "${C_DIM}  - grep-secrets.sh  HARD fail (AKB_*/Brevo/Stripe secrets bloquean)${C_OFF}"
echo "${C_DIM}  - grep-claims.sh   WARNING only (review manual)${C_OFF}"
echo "${C_DIM}  - gitleaks         HARD fail (si binary disponible localmente)${C_OFF}"
echo ""
echo "${C_DIM}Verify: bash bin/setup-pre-commit.sh --status${C_OFF}"
echo "${C_DIM}Skip ad-hoc: git commit --no-verify (NO recomendado)${C_OFF}"
